package com.hnagorny.request.utils;

/**
 * This class defines the coordinates to use to get a position.
 * @author hnagorny
 */

public class Coordinate {

    private Double x;
    private Double y;

    public Coordinate(Double x, Double y) {

        this.x = x;
        this.y = y;

    }

    public Double getX() {

        return x;

    }

    public Double getY() {

        return y;

    }

}
